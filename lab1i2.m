clc; % Очистка командного окна
clear; % Очистка переменных

% Инициализация параметров
t = linspace(-2, 2, 100); % Генерация 100 точек для параметра t от -2 до 2
x = t.^2; % Вычисление координаты x по уравнению x = t^2
y = (3/2) * t.^3; % Вычисление координаты y по уравнению y = (3/2)t^3

% Создание нового окна для графика с настройками
figure('Color', 'w', 'Position', [100, 100, 600, 600]); % Создание нового окна для графика: белый фон и размер 600x600 пикселей

% Построение графика
plot(x, y, 'b-', 'LineWidth', 2); % Построение графика: синяя сплошная линия с толщиной 2 пикселя

% Дополнительные настройки графика
title('Построение полукубической параболы (парабола Нейля)', 'FontSize', 16, 'FontWeight', 'bold', 'Color', 'b'); % Заголовок графика с синим цветом и увеличенным шрифтом
xlabel('x = t^2', 'FontSize', 14); % Подпись оси x
ylabel('y = (3/2)t^3', 'FontSize', 14); % Подпись оси y

% Настройка осей
grid on; % Включение сетки для улучшения восприятия графика
axis equal; % Установка равных масштабов для осей x и y
xlim([-1 5]); % Установка пределов для оси x
ylim([-5 5]); % Установка пределов для оси y
